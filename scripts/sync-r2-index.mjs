#!/usr/bin/env node
/**
 * Sync data/r2VideoIndex.ts from the configured R2 bucket (flat {id}.mp4 keys).
 * Usage: node scripts/sync-r2-index.mjs
 */

import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { ListObjectsV2Command, S3Client } from "@aws-sdk/client-s3";

function loadEnv() {
  const path = resolve(process.cwd(), ".env");
  try {
    const raw = readFileSync(path, "utf8");
    for (const line of raw.split(/\r?\n/)) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith("#")) continue;
      const eq = trimmed.indexOf("=");
      if (eq <= 0) continue;
      const key = trimmed.slice(0, eq).trim();
      let value = trimmed.slice(eq + 1).trim();
      if (
        (value.startsWith('"') && value.endsWith('"')) ||
        (value.startsWith("'") && value.endsWith("'"))
      ) {
        value = value.slice(1, -1);
      }
      if (!(key in process.env)) process.env[key] = value;
    }
  } catch {
    /* optional */
  }
}

loadEnv();

const accountId = process.env.R2_ACCOUNT_ID?.trim();
const accessKeyId = process.env.R2_ACCESS_KEY_ID?.trim();
const secretAccessKey = process.env.R2_SECRET_ACCESS_KEY?.trim();
const bucket = process.env.R2_BUCKET_NAME?.trim();

if (!accountId || !accessKeyId || !secretAccessKey || !bucket) {
  console.error("Missing R2_* env vars in .env");
  process.exit(1);
}

const client = new S3Client({
  region: "auto",
  endpoint: `https://${accountId}.r2.cloudflarestorage.com`,
  credentials: { accessKeyId, secretAccessKey },
});

const keys = [];
let token;

do {
  const out = await client.send(
    new ListObjectsV2Command({
      Bucket: bucket,
      Prefix: "",
      ContinuationToken: token,
      MaxKeys: 1000,
    })
  );
  for (const obj of out.Contents ?? []) {
    if (obj.Key) keys.push(obj.Key);
  }
  token = out.IsTruncated ? out.NextContinuationToken : undefined;
} while (token);

/** postId -> exact object key (e.g. "168" -> "168.MP4") */
const videoKeys = {};
for (const key of keys) {
  const m = key.match(/^(\d+)\.(mp4|MP4|mov|MOV|webm|WEBM|m4v|M4V)$/);
  if (!m) continue;
  videoKeys[m[1]] = key;
}

const postIds = Object.keys(videoKeys).sort((a, b) => Number(a) - Number(b));
const skipped = keys.filter((key) => !/^(\d+)\.(mp4|MP4|mov|MOV|webm|WEBM|m4v|M4V)$/.test(key));
const maxId = postIds.length ? Number(postIds[postIds.length - 1]) : 0;
const missing = [];
for (let n = 1; n <= maxId; n++) {
  if (!videoKeys[String(n)]) missing.push(n);
}
const outPath = resolve(process.cwd(), "data/r2VideoIndex.ts");
const body = `/**
 * Auto-generated by scripts/sync-r2-index.mjs — do not edit by hand.
 * Re-run after uploading new {postId}.mp4 files to R2.
 * Last sync: ${new Date().toISOString()}
 */
export const R2_VIDEO_KEYS: Record<string, string> = ${JSON.stringify(videoKeys, null, 2)};

export const R2_VIDEO_POST_IDS = new Set<string>(Object.keys(R2_VIDEO_KEYS));

export function hasR2Video(postId: string) {
  return R2_VIDEO_POST_IDS.has(postId.trim());
}

export function getR2VideoKey(postId: string) {
  return R2_VIDEO_KEYS[postId.trim()];
}
`;

writeFileSync(outPath, body, "utf8");
console.log(`Wrote ${outPath} (${postIds.length} videos synced from ${keys.length} bucket objects)`);
if (skipped.length) {
  console.log(`Skipped ${skipped.length} object(s) — name must be {postId}.mp4 (e.g. 21.mp4):`);
  for (const k of skipped) console.log(`  - ${k}`);
}
if (missing.length) {
  console.log(
    `Missing ${missing.length} post id(s) between 1–${maxId} (no file uploaded): ${missing.slice(0, 25).join(", ")}${missing.length > 25 ? ", …" : ""}`
  );
}
